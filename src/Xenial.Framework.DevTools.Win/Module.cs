using System;
using System.Collections.Generic;
using System.Linq;

using DevExpress.ExpressApp;
using DevExpress.ExpressApp.Win.SystemModule;

using Xenial.Framework.WebView.Win;

namespace Xenial.Framework.DevTools.Win;

/// <summary>
/// 
/// </summary>
[Xenial.XenialCollectControllers]
public static partial class XenialDevToolsWindowsFormsTypeList
{
    /// <summary>
    /// 
    /// </summary>
    /// <param name="types"></param>
    /// <returns></returns>
    public static IEnumerable<Type> UseXenialDevToolsWindowsFormsControllers(this IEnumerable<Type> types)
        => types.Concat(ControllerTypes);
}

/// <summary>
/// Class XenialDevToolsWindowsFormsModule.
/// Implements the <see cref="Xenial.Framework.XenialModuleBase" />
/// </summary>
/// <seealso cref="Xenial.Framework.XenialModuleBase" />
/// <autogeneratedoc />
[XenialCheckLicense]
public sealed partial class XenialDevToolsWindowsFormsModule : XenialModuleBase
{
    /// <summary>
    /// Adds the DevExpress.ExpressApp.SystemModule.SystemModule to the collection.
    /// </summary>
    ///
    /// <returns>   ModuleTypeList. </returns>

    protected override ModuleTypeList GetRequiredModuleTypesCore()
        => base.GetRequiredModuleTypesCore()
            .AndModuleTypes(new[]
            {
                typeof(XenialDevToolsModule),
                typeof(SystemWindowsFormsModule),
                typeof(XenialWebViewWindowsFormsModule)
            });

    /// <summary>
    /// 
    /// </summary>
    /// <returns></returns>
    protected override IEnumerable<Type> GetDeclaredControllerTypes()
        => base.GetDeclaredControllerTypes()
            .UseXenialDevToolsWindowsFormsControllers();
}
